/**
 * 04. Write a function that accepts a single argument, a number, and returns
 *     the result of multiplying its argument by an exponent of 2 (also known
 *     as squaring the number).
 * 
 *     squaredNumber(3); // 9
 */

function squaredNumber(num) {
  return num ** 2;
}

console.log(squaredNumber(3));