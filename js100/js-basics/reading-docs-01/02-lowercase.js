/**
 * 02. Find out whether JavaScript has a method to lowercase a string, for
 *     example converting 'Aloha, World!' into 'aloha, world!.
 */

/**
 * Yes - strings have the .toLowerCase() method, which returns the calling
 * string in entirely lowercase.
 */

console.log('Aloha, World'.toLowerCase());