/**
 * 04. Locate the documentation for the Array built-in
 *     object on MDN.
 * 
 *     How can we access the element 'and' in the array
 *     ['fish', 'and', 'chips']?
 */

/**
 * We can access 'and' in the array by using bracket notation and the index of
 * element 'and' within the array.
 * 
 * For this exercise, this would look like: ['fish', 'and', 'chips'][1]
 * (indexes start at zero - to get the second element, we use an index of 1)
 */

console.log(['fish', 'and', 'chips'][1]);
// => 'and'