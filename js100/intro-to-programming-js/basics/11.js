/**
 * 11. What value does the following expression evaluate to?
 *     > parseInt('3.1415')
 */

/**
 * This expression would evaluate to 3 (type Number). The parseInt function
 * parses strings that start with a digit, +, or -, and converts digits
 * following that initial character until it reaches a non-digit (in this case,
 * .).
 * 
 * To convert strings into decimal values, use the parseFloat() function instead.
 */