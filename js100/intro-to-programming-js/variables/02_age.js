/**
 * 02. Write a program named age.js that includes someone's age and then
 *     calculates and reports the future age in 10, 20, 30 and 40 years. Below
 *     is the output for someone 20 years old.
 * 
 *     You are 20 years old.
 *     In 10 years, you will be 30 years old.
 *     In 20 years, you will be 40 years old.
 *     In 30 years, you will be 50 years old.
 *     In 40 years, you will be 60 years old.
 */

let userAge = 20;

console.log(`You are ${userAge} years old.`);
console.log(`In 10 years, you will be ${10 + userAge} years old.`);
console.log(`In 20 years, you will be ${20 + userAge} years old.`);
console.log(`In 30 years, you will be ${30 + userAge} years old.`);
console.log(`In 40 years, you will be ${40 + userAge} years old.`);