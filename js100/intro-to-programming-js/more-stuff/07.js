/**
 * 07. What is exception handling and what does it solve?
 */

/**
 * Exception handling refers to the way that the Javascript engine handles and
 * manages errors when code is run.
 * 
 * It can solve a couple of different issues, one main issue being whether or
 * not the code and program can still run when the engine is faced with the
 * error. This can be managed through a try/catch block, where the try block
 * runs the code that may face an error, and if an error is faced, the catch
 * block is executed, which provides the programmer a means to figure out how
 * to deal with the error.
 */